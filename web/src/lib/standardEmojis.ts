/** Категории с эмодзи, чья версия Unicode не выше maxVersion. */
export function standardEmojis(maxVersion: number): Array<{ name: string; chars: string[] }> {
  return GROUPS.map(([name, sets]) => ({
    name,
    chars: sets.filter(([v]) => v <= maxVersion).flatMap(([, s]) => s.split(' ')),
  })).filter((g) => g.chars.length > 0);
}
